package base;

public class binary {
    public static void main(String[] args) {
        int i = 10; // 10
        int j = 011; // 8
        int k = 0x10; // 16
        System.out.println(i);
        System.out.println(j);
        System.out.println(k);

        float f = 1.23f;
        double d = 1.23;
        System.out.println(f==d);
    }
}
