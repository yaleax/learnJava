package com.suan.operator;
public class Demo04 {
    public static void main(String[] args) {
        int a = 3;
        int b = a ++;
        int c = ++ a;
        System.out.println(a);
        System.out.println(a);
        System.out.println(b);
        System.out.println(b);
        System.out.println(c);
        System.out.println(c);
        double x = Math.pow(2, 3);
        System.out.println(x);
    }
}