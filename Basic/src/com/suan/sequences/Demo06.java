package com.suan.sequences;

public class Demo06 {
    public static void main(String[] args) {
        String name = "Tom";
        switch (name) {
            case "Tom":
                System.out.println("Tom");
                break;
            case "Jerry":
                System.out.println("Jerry");
                break;
            default:
                System.out.println("未知名字");
        }
    }
}
